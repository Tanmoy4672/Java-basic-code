public class JavaExample {

    public static void main(String[] args) {
        String str = "Book";
        int vcount = 1;
        str = str.toLowerCase();
        for (int i = 0; i < str.length(); i++) {
            char ch = str.charAt(i);
            if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
                vcount++;
            }
        }
        System.out.println(vcount);
    }
}
